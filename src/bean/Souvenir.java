package bean;

public class Souvenir {
    private int souvenirId;
    private String souvenirSeasons;
    private String souvenirName;
    private String souvenirContent;
    private String souvenirPhoto;

    public int getSouvenirId() {
        return souvenirId;
    }
    public void setSouvenirId(int souvenirId) {
        this.souvenirId = souvenirId;
    }

    public String getSouvenirSeasons() {
        return souvenirSeasons;
    }
    public void setSouvenirSeasons(String souvenirSeasons) {
        this.souvenirSeasons = souvenirSeasons;
    }

    public String getSouvenirName() {
        return souvenirName;
    }
    public void setSouvenirName(String souvenirName) {
        this.souvenirName = souvenirName;
    }

    public String getSouvenirContent() {
        return souvenirContent;
    }
    public void setSouvenirContent(String souvenirContent) {
        this.souvenirContent = souvenirContent;
    }

    public String getSouvenirPhoto() {
        return souvenirPhoto;
    }
    public void setSouvenirPhoto(String souvenirPhoto) {
        this.souvenirPhoto = souvenirPhoto;
    }
}
